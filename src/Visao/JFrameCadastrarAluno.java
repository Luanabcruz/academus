    private void jB_cadastrarDisciplinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_cadastrarDisciplinaActionPerformed

        model.setMatricula(Integer.parseInt(jT_matriculaAluno.getText()));
        model.setCursoCod((int) jComboBox_cursoAluno.getSelectedItem());
        model.setAnoIngressante(jT_anoDeIngressoAluno.getText());
        model.setCra(Float.parseFloat(jT_craAluno.getText()));
        try {
            controle.cadastrarAluno(model);

            JOptionPane.showMessageDialog(rootPane, "Cadastrado com sucesso!");

            jT_matriculaAluno.setText("");
            jComboBox_cursoAluno.getSelectedItem();
            jT_anoDeIngressoAluno.setText("");
            jT_craAluno.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error ao cadastrar no banco de dados!");
        }

    }//GEN-LAST:event_jB_cadastrarDisciplinaActionPerformed

